    public void getConnectedProfile(){
            network = new NetWorkProfiles(new CheckConnected().getConnectedProfile());
            if(connectedProfile!=null){
                String prevConnect = connectedProfile.getName();
            }
            connectedProfile=network.getConnected();
            jLabel5.setText(connectedProfile.getName());
            jLabel2.setText(connectedProfile.getPassword());
            try{
                WifiQr qrCode = new WifiQr(connectedProfile);
                setImage(qrCode);
            }
            catch(FileNotFoundException ex){
                System.out.println("File Not Found for the qr code image");
            }
            profiles=network.getWifiNetworkList();
            names = new String[profiles.size()];
            for (int i = 0; i < profiles.size(); i++) {
                names[i] = profiles.get(i).getName();
            }
            jList1.setModel(new javax.swing.AbstractListModel<String>() {
                @Override
                public int getSize() { return names.length; }
                @Override
                public String getElementAt(int i) { return names[i]; }
            });
            jList1.clearSelection();
            jList1.repaint();
            jList1.revalidate();
           
            TitledBorder titledBorder = (TitledBorder) jPanel3.getBorder();
            titledBorder.setTitle("Κωδικός Συνδεδεμένου Wifi");
            jPanel3.setBorder(titledBorder);
            titledBorder = (TitledBorder) jPanel4.getBorder();
            titledBorder.setTitle("QR Code Συνδεδεμένου Wifi");
            jPanel4.setBorder(titledBorder);
            jPanel3.repaint();
            jPanel3.revalidate();
            jPanel4.repaint();
            jPanel4.revalidate();
            this.repaint();
            this.revalidate();
           
            connectedMessage();
            
    }
